class OUIDatabase:
    """OUI vendor lookup from MAC address prefix.

    Maps the first 3 bytes of a MAC address to the registered
    vendor/manufacturer name using an embedded database.
    """

    def __init__(self):
        self._db = dict(_OUI_DATABASE)

    def lookup(self, mac_address: str) -> Optional[str]:
        """Look up the vendor for a MAC address.

        Args:
            mac_address: MAC address in any common format
                (XX:XX:XX:XX:XX:XX, XX-XX-XX-XX-XX-XX, or XXXX.XXXX.XXXX).

        Returns:
            Vendor name string, or None if the OUI is not found.
        """
        # Normalize MAC address to XX:XX:XX format
        normalized = self._normalize_mac(mac_address)
        if not normalized:
            return None
        oui = normalized[:8].upper()  # "XX:XX:XX"
        return self._db.get(oui)

    def lookup_bytes(self, mac_bytes: bytes) -> Optional[str]:
        """Look up vendor from raw MAC address bytes.

        Args:
            mac_bytes: 6 bytes representing a MAC address.

        Returns:
            Vendor name string, or None if not found.
        """
        if len(mac_bytes) < 3:
            return None
        oui = ":".join(f"{b:02X}" for b in mac_bytes[:3])
        return self._db.get(oui)

    def add_entry(self, oui: str, vendor: str) -> None:
        """Add or update an OUI entry.

        Args:
            oui: OUI in XX:XX:XX format.
            vendor: Vendor name.
        """
        normalized = oui.upper().replace("-", ":")
        self._db[normalized] = vendor

    def get_all_vendors(self) -> List[str]:
        """Return a sorted list of all unique vendor names."""
        return sorted(set(self._db.values()))

    def get_ouis_for_vendor(self, vendor: str) -> List[str]:
        """Return all OUIs registered to a vendor.

        Args:
            vendor: Vendor name (case-insensitive partial match).

        Returns:
            List of OUI strings matching the vendor.
        """
        vendor_lower = vendor.lower()
        return [
            oui for oui, v in self._db.items()
            if vendor_lower in v.lower()
        ]

    def get_vendor_count(self) -> int:
        """Return the number of unique vendors in the database."""
        return len(set(self._db.values()))

    def get_oui_count(self) -> int:
        """Return the total number of OUI entries."""
        return len(self._db)

    @staticmethod
    def _normalize_mac(mac: str) -> Optional[str]:
        """Normalize a MAC address to XX:XX:XX:XX:XX:XX format."""
        mac = mac.strip().upper()
        # Handle different separators
        for sep in [":", "-", "."]:
            mac = mac.replace(sep, ":")
        # Handle no separator
        if ":" not in mac and len(mac) == 12:
            mac = ":".join(mac[i:i+2] for i in range(0, 12, 2))
        parts = mac.split(":")
        if len(parts) != 6:
            return None
        try:
            return ":".join(f"{int(p, 16):02X}" for p in parts)
        except ValueError:
            return None
