def generate_wps_pin() -> str:
    """Generate a random valid WPS PIN with checksum.

    WPS PINs are 8 digits: first 7 are random, the 8th is a checksum
    digit computed from the first 7.

    Returns:
        An 8-digit WPS PIN string with valid checksum.
    """
    pin_digits = [random.randint(0, 9) for _ in range(7)]
    checksum = _compute_checksum(pin_digits)
    pin_digits.append(checksum)
    return "".join(str(d) for d in pin_digits)


def validate_wps_pin(pin: str) -> bool:
    """Validate a WPS PIN checksum.

    Args:
        pin: An 8-digit WPS PIN string.

    Returns:
        True if the PIN has a valid checksum, False otherwise.
    """
    if not pin or len(pin) != 8:
        return False
    if not pin.isdigit():
        return False
    digits = [int(d) for d in pin[:7]]
    expected_checksum = _compute_checksum(digits)
    return expected_checksum == int(pin[7])


def _compute_checksum(digits: list) -> int:
    """Compute the WPS PIN checksum digit.

    The checksum algorithm:
    1. Accumulate sum: for digit at even index (0-based), add digit;
       for digit at odd index, add 3 * digit.
    2. Checksum = (10 - (sum % 10)) % 10.

    Args:
        digits: List of 7 integer digits.

    Returns:
        Single checksum digit (0-9).
    """
    total = 0
    for i, d in enumerate(digits):
        if i % 2 == 0:
            total += d
        else:
            total += 3 * d
    return (10 - (total % 10)) % 10
